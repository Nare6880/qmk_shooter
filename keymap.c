#define RAISE MO(_RAISE)
#define LOWER MO(_LOWER)

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case GM_INV:
            // Toggle gaming mode & clear OLED display
            if (!record->event.pressed) {
                toggleGamingMode();
                if (isGamingMode()) {
                    readMainTimer();
                    initGame();
                    startGame();
                }
               clear_display();
            }
           break;
        case KC_LEFT:
            if (record->event.pressed) {
                if (isGamingMode()) {
                    movePlayer(1); // 1 = isLeft
                    return false;
                }
            }
            break;
        case KC_RIGHT:
            if (record->event.pressed) {
                if (isGamingMode()) {
                    movePlayer(0); // 0 = isRight
                    return false;
                }
            }
            break;
        case KC_ENT:
            if (record->event.pressed) {
                if (isGamingMode()) {
                    firePlayerBeam();
                    return false;
                }
            }
            break;
        case KC_LGUI:
            if (record->event.pressed) {
                if (isGamingMode()) {
                    return false;
                }
            }
            break;
     }
     return true;
}
